void drawChar(u8* framebuffer, char c, int startX, int startY, u8 r, u8 g, u8 b) {
    int index = -1;
    if (c >= 32 && c <= 122) {
        index = c - 32;
    }
    
    if (index < 0) return; // Character not in font
    
    // Draw each pixel of the character
    for (int y = 0; y < 7; y++) {
        u8 row = font[index][y];
        for (int x = 0; x < 5; x++) {
            if (row & (1 << (4 - x))) { // Check if bit is set (right to left)
                int px = startX + x;
                int py = startY + y;
                if (px >= 0 && px < SCREEN_WIDTH && py >= 0 && py < SCREEN_HEIGHT) {
                    // Convert to framebuffer coordinates and write BGR pixel
                    int offset = (px * FB_WIDTH + (FB_HEIGHT - 1 - py)) * 3;
                    if (offset >= 0 && offset < FB_WIDTH * FB_HEIGHT * 3 - 2) {
                        framebuffer[offset + 0] = b;
                        framebuffer[offset + 1] = g;
                        framebuffer[offset + 2] = r;
                    }
                }
            }
        }
    }
}

void drawString(u8* framebuffer, const char* str, int x, int y, u8 r, u8 g, u8 b) {
    int cursorX = x;
    int cursorY = y;
    
    while (*str) {
        if (*str == '\n') {
            cursorX = x;
            cursorY += 9; // Move down to next line (7 + 2 spacing)
        } else {
            drawChar(framebuffer, *str, cursorX, cursorY, r, g, b);
            cursorX += 6; // Move right to next character (5 + 1 spacing)
        }
        str++;
    }
}
